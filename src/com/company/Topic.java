package com.company;

public class Topic {
    private double number;
    private String name;
    private int yearGroup;
    private int term;
    private int week;
    private int lesson;

    public Topic(double number,String name, int yearGroup, int term, int week, int lesson) {
        this.number = number;
        this.name = name;
        this.yearGroup = yearGroup;
        this.term = term;
        this.week = week;
        this.lesson = lesson;
        
    }
}
